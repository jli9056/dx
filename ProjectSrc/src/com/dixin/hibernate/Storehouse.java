package com.dixin.hibernate;

import java.util.Set;

/**
 * Storehouse entity.
 * 
 * 
 */
public class Storehouse extends AbstractStorehouse implements
		java.io.Serializable {

	// Constructors

	/**
	 * 
	 */
	private static final long serialVersionUID = -6216677055448312719L;

	/** default constructor */
	public Storehouse() {
	}

	/** minimal constructor */
	public Storehouse(String name, Integer shippable) {
		super(name, shippable);
	}

	/** full constructor */
	public Storehouse(String name, String address, Integer shippable,
			String comment, Set repertories, Set arrivedetails, Set reserveds) {
		super(name, address, shippable, comment, repertories, arrivedetails,
				reserveds);
	}

}
