package com.dixin.hibernate;

import java.util.Set;

/**
 * Employee entity.
 * 
 * 
 */
public class Employee extends AbstractEmployee implements java.io.Serializable {

	// Constructors

	/**
	 * 
	 */
	private static final long serialVersionUID = 3164445111679161657L;

	/** default constructor */
	public Employee() {
	}

	/** minimal constructor */
	public Employee(Shop shop, String name, Integer sex) {
		super(shop, name, sex);
	}

	/** full constructor */
	public Employee(Shop shop, String name, Integer sex, String phone,
			String comment, Set deliveries, Set arrivements, Set factoryorders) {
		super(shop, name, sex, phone, comment, deliveries, arrivements,
				factoryorders);
	}
}
