package com.dixin.hibernate;

import java.util.Set;

/**
 * Menu entity.
 * 
 * @author MyEclipse Persistence Tools
 */
public class Menu extends AbstractMenu implements java.io.Serializable {

	// Constructors

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/** default constructor */
	public Menu() {
	}

	/** minimal constructor */
	public Menu(String text) {
		super(text);
	}

	/** full constructor */
	public Menu(Menu menu, Integer sort, String text, String url,
			String description, Set roles, Set actions, Set menus) {
		super(menu, sort, text, url, description, roles, actions, menus);
	}

}
