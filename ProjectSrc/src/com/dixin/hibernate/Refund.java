package com.dixin.hibernate;

import java.util.Date;

/**
 * Refund entity.
 * 
 */
public class Refund extends AbstractRefund implements java.io.Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 3647808110297502477L;

	// Constructors

	/** default constructor */
	public Refund() {
	}

	/** minimal constructor */
	public Refund(Deliverydetail deliverydetail, Integer quantity,
			Integer finished, Date refundDate) {
		super(deliverydetail, quantity, finished, refundDate);
	}

	/** full constructor */
	public Refund(Deliverydetail deliverydetail, Integer quantity,
			String method, Integer finished, Date refundDate) {
		super(deliverydetail, quantity, method, finished, refundDate);
	}

}
