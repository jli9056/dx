package com.dixin.hibernate;

import java.util.Date;
import java.util.Set;

/**
 * Arrivement entity.
 * 
 * 
 */
public class Arrivement extends AbstractArrivement implements
		java.io.Serializable {

	// Constructors

	/**
	 * 
	 */
	private static final long serialVersionUID = 4517163090559324210L;

	/** default constructor */
	public Arrivement() {
	}

	/** minimal constructor */
	public Arrivement(String arriveNo, Employee employee,
			Date arriveDate) {
		super(arriveNo, employee, arriveDate);
	}

	/** full constructor */
	public Arrivement(String arriveNo, Employee employee,
			Date arriveDate, Set arrivedetails) {
		super(arriveNo, employee, arriveDate, arrivedetails);
	}

}
