package com.dixin.hibernate;

/**
 * Reserved entity.
 * 
 */
public class Reserved extends AbstractReserved implements java.io.Serializable {

	// Constructors

	/**
	 * 
	 */
	private static final long serialVersionUID = -2243509491530754072L;

	/** default constructor */
	public Reserved() {
	}

	/** minimal constructor */
	public Reserved(Storehouse storehouse, Orderdetail orderdetail) {
		super(storehouse, orderdetail);
	}

	/** full constructor */
	public Reserved(Storehouse storehouse, Orderdetail orderdetail,
			Integer reservedCount, String orderNo) {
		super(storehouse, orderdetail, reservedCount, orderNo);
	}

	public Reserved(Storehouse storehouse, Orderdetail orderdetail,
			Integer reservedCount) {
		super(storehouse, orderdetail, reservedCount);
	}
}
