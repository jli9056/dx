package com.dixin.hibernate;

import java.util.Date;

/**
 * Deliverydetail entity.
 * 
 *
 */
public class Deliverydetail extends AbstractDeliverydetail implements
		java.io.Serializable {

	// Constructors

	/**
	 * 
	 */
	private static final long serialVersionUID = 7562843554622997410L;

	/** default constructor */
	public Deliverydetail() {
	}

	/** minimal constructor */
	public Deliverydetail(Delivery delivery, Product product, Order order,
			Integer quantity, Date queueTime) {
		super(delivery, product, order, quantity, queueTime);
	}

	/** full constructor */
	public Deliverydetail(Delivery delivery, Product product, Order order,
			Integer quantity, Date queueTime, String status, String comment,
			String orderNo, String customerName, Date orderDate,
			String employee, Date deliverDate) {
		super(delivery, product, order, quantity, queueTime, status, comment,
				orderNo,customerName, orderDate, employee, deliverDate);
	}

}
